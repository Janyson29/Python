#Inteiro
numero_inteiro = 3
print("Inteiro =", + numero_inteiro)

#Real com ponto flutuante
numero_real = 3.14
print('Real com flutuante =', + numero_real)

#Type()
print('Tipo da variavel inteira =', type(numero_inteiro))
print('Tipo da variavel real com flutuante =', type(numero_real))

#Soma +
soma = 1 + 1
print("1 + 1 =", soma)

#Subtração -
subtracao = 1 - 1
print("1 - 1 =", subtracao)

#Multiplicação *
multiplicacao = 2 * 2
print('2 x 2 =', multiplicacao)

#Divisão /
divisao = 5 / 2
print('5 / 2 =', divisao)
print('Tipo da variavel do resultado da divisao = ', type(divisao))

#int()
print('Valor da divisao em inteiro = ', int(divisao))

#float()
print('Valor da soma em Float = ', float(soma))

#Modulo %
modulo = 5 % 2
print('Modulo = ', modulo)

#Divisão inteiro //
divisao = 5 // 2
print('5 // 2 =', divisao)
print('Tipo da variavel do resultado da divisao = ', type(divisao))